#--- Impressão de texto em multiplas linhas com caractere de escape \n
print('Olá, Flavia!\nSeja bem-vinda')

#--- Impressão de texto em multiplas linhas usando aspas triplas
print('''Olá, Flavia!
Seja bem-vinda''')

#--- Impressão de texto com tabulação com caractere de escape \t
print('Olá, Flavia!\tSeja bem-vinda')

#--- Impressão de texto com aspas simples com caractere de escape \'
print('Olá, Flavia!\'Seja bem-vinda\'')

#--- Impressão de texto com aspas duplas com caractere de escape \"
print("Olá, Flavia!\"Seja bem-vinda\"")

#--- Impressão de texto com barra invertida com caractere de escape \\
print('Olá, Flavia!\\Seja bem-vinda\\')

#--- Impressão de texto com caractere de escape \r para retorno
print('Olá, Flavia!\rSeja bem-vinda')

#--- Impressão de texto com caractere de escape \v para vertical tab
print('Olá, Flavia!\vSeja bem-vinda\vao nosso sistema')

#--- Impressão de texto com caractere de escape \b para backspace
print('Olá, Flavia!\b Seja bem-vinda')

#--- Impressão com multiplicação de caracteres
barra_cabecalho = '='*10
print(barra_cabecalho,'Sistema da Flavinha',barra_cabecalho)